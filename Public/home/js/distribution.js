function showtime(){
	if(document.getElementById("time").style.display="none")
	{
		document.getElementById("time").style.display="block";
	}
}
function hidetime(){
	if(document.getElementById("time").style.display="block")
	{
		document.getElementById("time").style.display="none";
	}
}
